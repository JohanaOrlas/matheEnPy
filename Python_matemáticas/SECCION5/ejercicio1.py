#Ciclo que imprime los números de 0 a 100 de forma ascendente
for i in range(0,101):
    print(i)

print("Ahora los números del 0 al 100 de forma descendente")
#Ciclo que imprime los números de 0 a 100 de forma descendente
for i in range(101,0,-1):
    print(i)

